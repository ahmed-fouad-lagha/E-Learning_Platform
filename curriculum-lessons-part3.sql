**Content:**
Jean-Baptiste Poquelin, known as Molière (1622-1673), created the golden age of French comedy through his brilliant social satire and memorable characters.

**Molière''s Innovation:**
1. **Character Comedy**: Focus on psychological types and social roles
2. **Social Satire**: Criticism of contemporary society and customs
3. **Universal Types**: Characters representing eternal human follies
4. **Moral Purpose**: Comedy should correct vices through laughter

**Major Works:**

**Tartuffe (1664):**
- Satirizes religious hypocrisy
- Tartuffe manipulates the pious Orgon through false devotion
- Controversy and censorship due to religious themes
- Themes: appearance vs. reality, blind faith, family conflicts

**Le Misanthrope (1666):**
- Alceste''s hatred of social hypocrisy and his love for the coquettish Célimène
- Explores the conflict between idealism and social reality
- Questions whether complete honesty is possible or desirable in society

**L''Avare (1668):**
- Harpagon''s obsession with money destroys family relationships
- Based on Plautus'' "Aulularia"
- Explores how vice corrupts natural affections

**Le Bourgeois Gentilhomme (1670):**
- Monsieur Jourdain''s comic attempts to become a gentleman
- Satirizes social climbing and pretension
- Famous "prose" scene: Jourdain discovers he speaks prose

**Les Femmes Savantes (1672):**
- Satirizes intellectual pretension among women
- Philaminte and her daughters'' obsession with learning
- Balances criticism with respect for genuine education

**Comic Techniques:**
1. **Character Types**: The miser, hypocrite, misanthrope, pedant
2. **Repetition**: Verbal and gestural comedy through repetition
3. **Irony**: Dramatic irony where audience knows more than characters
4. **Farce Elements**: Physical comedy, mistaken identity, disguise
5. **Rhythm**: Perfectly timed comic situations and dialogue

**Language and Style:**
- Natural, conversational dialogue
- Perfect balance of verse and prose
- Memorable maxims and witticisms
- Adaptation to different social classes

**Social Criticism:**
Molière critiques:
- Religious hypocrisy and extremism
- Social pretension and climbing
- Educational pedantry
- Medical charlatanism
- Male tyranny and women''s roles

**Influence:**
Molière''s comedies established the template for social comedy and influenced dramatists worldwide, from Sheridan to Shaw to contemporary writers.

