'**Learning Objectives:**
- Contrast Racine''s psychological approach with Corneille''s heroic style
- Analyze the role of passion and fate in Racinian tragedy
- Examine Racine''s use of classical mythology and biblical sources

**Content:**
Jean Racine (1639-1699) perfected French classical tragedy by focusing on the inner psychological torment of his characters.

**Racinian vs. Cornelian Tragedy:**

**Racine''s Innovations:**
1. **Psychological Depth**: Focus on inner emotional conflict
2. **Fatalism**: Characters trapped by passion and destiny
3. **Simplicity**: Streamlined plots, minimal external action
4. **Poetic Language**: Musical, flowing alexandrine verse
5. **Female Protagonists**: Strong, complex female characters

**Major Works:**

**Andromaque (1667):**
- Chain of unrequited love: Oreste loves Hermione, who loves Pyrrhus, who loves Andromaque
- Explores jealousy, obsession, and the destructive power of passion
- Each character is both victim and tormentor

**Britannicus (1669):**
- Political and personal rivalry between Nero and Britannicus
- Nero''s transformation from potential good ruler to tyrant
- Agrippine''s maternal ambition and loss of influence

**Bérénice (1670):**
- Based on Roman history: Titus must renounce Bérénice for reasons of state
- Explores the tragedy of impossible love
- Famous for its emotional restraint and elegiac tone

**Phèdre (1677):**
- Racine''s masterpiece, based on Euripides'' Hippolytus
- Phèdre''s guilty passion for her stepson Hippolyte
- Themes: forbidden love, guilt, divine punishment, self-destruction

**Key Themes:**
1. **Passion as Destructive Force**: Love as madness and suffering
2. **Fate and Predestination**: Characters unable to escape their destiny
3. **Guilt and Redemption**: Christian influences on pagan themes
4. **Power and Corruption**: Political themes intertwined with personal
5. **Mother-Child Relationships**: Complex family dynamics

**Racine''s Style:**
- **Alexandrine Verse**: Twelve-syllable lines with strict rhyme scheme
- **Confidants**: Characters who receive confessions and provide counsel
- **Unity of Action**: Single, focused dramatic conflict
- **Bienséances**: Respect for propriety and good taste

**Language and Poetry:**
Racine''s verse achieves perfect harmony between form and meaning, with:
- Musical rhythm and flow
- Rich metaphorical language
- Emotional intensity within classical restraint

**Christian Influences:**
Despite pagan settings, Racine''s tragedies reflect his Jansenist background:
- Sense of human frailty and sin
- Predestination and divine justice
- Redemption through suffering

**BAC-Style Question:**
Analyze the character of Phèdre as a tragic heroine. How does Racine use her internal conflict to explore themes of guilt, passion, and fate?',

