البرمجة هي عملية إنشاء تعليمات للحاسوب لحل المشاكل وأداء المهام.

**مفاهيم البرمجة الأساسية:**

**1. الخوارزمية:**
إجراء خطوة بخطوة لحل مشكلة أو إكمال مهمة.

**مثال - إعداد الشاي:**
1. املأ الغلاية بالماء
2. شغل الغلاية
3. انتظر حتى يغلي الماء
4. ضع كيس الشاي في الكوب
5. اسكب الماء الساخن في الكوب
6. انتظر 3-5 دقائق
7. أخرج كيس الشاي
8. أضف السكر/الحليب إذا رغبت

**2. البرنامج:**
خوارزمية مكتوبة بلغة برمجة يمكن للحاسوب تنفيذها.

**3. الإدخال والإخراج:**
- **الإدخال**: البيانات المقدمة للبرنامج (مثل إدخال المستخدم، بيانات الملف)
- **الإخراج**: النتائج التي ينتجها البرنامج (مثل النص المعروض، الملف المحفوظ)

**4. المتغيرات:**
مواقع تخزين مسماة للبيانات التي يمكن أن تتغير أثناء تنفيذ البرنامج.
مثال: العمر = 17، الاسم = "أحمد"، الصف = "نهائي"

**5. أنواع البيانات:**
- **العدد الصحيح**: الأرقام الكاملة (1، 42، -5)
- **العدد العشري**: الأرقام العشرية (3.14، -2.5)
- **النص**: النص ("مرحبا"، "البكالوريا")
- **المنطقي**: قيم صحيح أو خطأ

**عملية تصميم الخوارزمية:**

**الخطوة 1: تحليل المشكلة**
- فهم ما يحتاج إلى حله
- تحديد المدخلات والمخرجات المطلوبة
- النظر في القيود والحالات الخاصة

**الخطوة 2: تصميم الخوارزمية**
- تقسيم المشكلة إلى خطوات أصغر
- ترتيب الخطوات بترتيب منطقي
- التأكد من معالجة جميع الحالات

**الخطوة 3: تمثيل الخوارزمية**
- الكتابة بلغة طبيعية
- إنشاء مخطط انسيابي
- كتابة كود وهمي

**المخططات الانسيابية:**

**الرموز الأساسية:**
- **بيضاوي**: البداية/النهاية
- **مستطيل**: عملية/إجراء
- **معين**: قرار/سؤال
- **مستطيل مائل**: إدخال/إخراج
- **أسهم**: اتجاه التدفق

**مثال - مخطط انسيابي لحساب الدرجة:**
```
[Start] → [Input: Test Score] → [Score ≥ 60?] 
                                    ↓
                              [Yes] → [Output: "Pass"]
                                    ↓
                              [No] → [Output: "Fail"]
                                    ↓
                                 [End]
```

**الكود الوهمي:**

**مثال - إيجاد أكبر ثلاثة أرقام:**
```
BEGIN
  INPUT number1, number2, number3
  SET max = number1
  
  IF number2 > max THEN
    SET max = number2
  END IF
  
  IF number3 > max THEN
    SET max = number3
  END IF
  
  OUTPUT "The maximum is: " + max
END
```

**الهياكل التحكمية:**

**1. التتابع**: تنفيذ التعليمات بترتيبها
**2. الاختيار (الشرطية)**: 
   - عبارات IF-THEN-ELSE
   - اتخاذ القرارات بناءً على الشروط

**3. التكرار (الحلقات)**:
   - حلقات FOR: تكرار عدد محدد من المرات
   - حلقات WHILE: تكرار طالما الشرط صحيح

**أمثلة على الخوارزميات:**

**المثال 1 - مجموع أول N عدد طبيعي:**
```
BEGIN
  INPUT n
  SET sum = 0
  SET i = 1
  
  WHILE i <= n DO
    SET sum = sum + i
    SET i = i + 1
  END WHILE
  
  OUTPUT "Sum is: " + sum
END
```

**المثال 2 - التحقق مما إذا كان الرقم أوليًا:**
```
BEGIN
  INPUT number
  SET isPrime = true
  
  IF number <= 1 THEN
    SET isPrime = false
  ELSE
    SET i = 2
    WHILE i * i <= number DO
      IF number MOD i = 0 THEN
        SET isPrime = false
        BREAK
      END IF
      SET i = i + 1
    END WHILE
  END IF
  
  IF isPrime THEN
    OUTPUT number + " is prime"
  ELSE
    OUTPUT number + " is not prime"
  END IF
END
```

**كفاءة الخوارزمية:**

**تعقيد الوقت**: المدة التي تستغرقها الخوارزمية بالنسبة لحجم المدخلات
**تعقيد الفضاء**: مقدار الذاكرة التي تستخدمها الخوارزمية

**أمثلة للمقارنة:**
- البحث الخطي: تحقق من كل عنصر واحدًا تلو الآخر - O(n)
- البحث الثنائي: نهج القسمة والت conquer - O(log n)

**خصائص الخوارزمية الجيدة:**
1. **الصحة**: تنتج مخرجات صحيحة لجميع المدخلات الصحيحة
2. **الكفاءة**: تستخدم وقتًا ومكانًا معقولين
3. **الوضوح**: سهلة الفهم والتعديل
4. **العمومية**: تعمل مع مجموعة من المدخلات

**أنواع الخوارزميات الشائعة:**
- **البحث**: العثور على بيانات معينة
- **الفرز**: ترتيب البيانات
- **الرياضية**: الحسابات والعمليات الحسابية
- **الرسم البياني**: العمل مع البيانات المترابطة
- **السلسلة**: معالجة النصوص

