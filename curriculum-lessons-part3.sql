**تمرين 1**: صمم خوارزمية لحساب مضاعف عدد
**تمرين 2**: أنشئ مخطط انسيابي لتحديد ما إذا كانت السنة سنة كبيسة
**تمرين 3**: اكتب كودًا وهميًا لإيجاد متوسط درجات الطلاب

