**Literary Forms:**
- **Tragedy**: Noble characters, fatal flaws, catharsis
- **Comedy**: Social satire, character types, moral lessons
- **Fables**: Allegorical stories with moral teachings

**Historical Context:**
The reign of Louis XIV (Sun King) created a centralized culture centered on Versailles. Literature served to glorify the monarchy while exploring universal human themes.

